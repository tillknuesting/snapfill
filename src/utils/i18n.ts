// Tiny in-app i18n. No runtime dep — a flat string table per language plus
// a `t()` helper that falls back to English when a key isn't translated.
// Adding a language: add the code to LANGS, fill in the dictionary below,
// and the picker UI surfaces it automatically.

export type Lang =
  | 'en' | 'zh' | 'hi' | 'es' | 'fr' | 'ar' | 'bn' | 'ru' | 'pt' | 'id' | 'de' | 'ja'

// Listed roughly by global speaker count (top 10) plus German and Japanese.
// Each entry shows the native name so it's recognisable in any UI language.
export const LANGS: { code: Lang; label: string; rtl?: boolean }[] = [
  { code: 'en', label: 'English' },
  { code: 'zh', label: '中文' },
  { code: 'hi', label: 'हिन्दी' },
  { code: 'es', label: 'Español' },
  { code: 'fr', label: 'Français' },
  { code: 'ar', label: 'العربية', rtl: true },
  { code: 'bn', label: 'বাংলা' },
  { code: 'ru', label: 'Русский' },
  { code: 'pt', label: 'Português' },
  { code: 'id', label: 'Bahasa Indonesia' },
  { code: 'de', label: 'Deutsch' },
  { code: 'ja', label: '日本語' },
]

export function isRTL(lang: Lang): boolean {
  return !!LANGS.find((l) => l.code === lang)?.rtl
}

const STORAGE_KEY = 'pdfhelper.lang'
const SUPPORTED = new Set<Lang>(LANGS.map((l) => l.code))

export function detectLang(): Lang {
  // Manual override wins.
  try {
    const stored = localStorage.getItem(STORAGE_KEY) as Lang | null
    if (stored && SUPPORTED.has(stored)) return stored
  } catch { /* ignore */ }
  // Fall back to browser locale, taking just the language subtag.
  const nav = typeof navigator !== 'undefined' ? navigator.language || '' : ''
  const code = nav.slice(0, 2).toLowerCase() as Lang
  return SUPPORTED.has(code) ? code : 'en'
}

export function persistLang(lang: Lang) {
  try { localStorage.setItem(STORAGE_KEY, lang) } catch { /* ignore */ }
}

